package ablw.projetprototypageinterfaceablw;

public class Sejour {
    public String titre;
    public String hote;
    public String description;
    public String tache;

    public Sejour(String titre, String hote, String description, String tache) {
        this.titre = titre;
        this.hote = hote;
        this.description = description;
        this.tache = tache;
    }
}
